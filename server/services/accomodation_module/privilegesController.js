//////////////////////////////////////////
/////////TEMPORARY OBJECT TO EXPORT//////
////////////////////////////////////////
var PublicVar = {};

//////////////////////////////////////////
/////////REQUIRING ALL FILES/////////////
////////////////////////////////////////
var models = require('../../config/modelsConfig');
//////////////////////////////////////////
/////////CALLING ALL MODELS//////////////
////////////////////////////////////////
var privileges = models.privileges;

//////////////////////////////////////////
/////////USING ALL QUERY METHODS/////////
////////////////////////////////////////
var privileges_info = privileges.selectQuery;
var privileges_insert = privileges.insertQuery;

///////////////////////////////////////////////////////////////
////////////SERVICE FOR PRIVILEGES INSERT/////////////////////
/////////////////////////////////////////////////////////////
PublicVar.postPrivilegesInsert = function(req, res, next) {
    var insertdata = {
        name: req['body']['name'].trim()
    };
    privileges_insert(res, insertdata, function() {
        return res.send({
            status: 1,
            msg: "Information Inserted Successfully"
        });
    });
}

///////////////////////////////////////////////////////////////
////////////SERVICE FOR PRIVILEGES LIST///////////////////////
/////////////////////////////////////////////////////////////
PublicVar.getPrivilegesList = function(req, res, next) {
    privileges_info(res, {}, function(privileges) {
        return res.send({
            status: 1,
            msg: "Information Fetched Successfully",
            privileges_list: privileges
        });
    });
}
////////////////////////////////////////
///////////EXPORTING TEMPORARY OBJECT//
//////////////////////////////////////
module.exports = PublicVar;